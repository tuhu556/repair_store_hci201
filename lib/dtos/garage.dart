class Garage {
  final int id;
  final String name;
  final String image;
  final String address;
  final int time;
  final double distance;
  final int rate;
  Garage({
    required this.id,
    required this.name,
    required this.image,
    required this.address,
    required this.time,
    required this.distance,
    required this.rate,
  });
  
}
